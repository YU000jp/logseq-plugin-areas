class Area
{
    name;
    color;
    shapes;

    constructor(_name, _color, _shapes){
        this.name = _name;
        this.color = _color;
        this.shapes = _shapes;
    }
}

class Shape
{
    name;
    points;

    constructor(_name, _points){
        this.name = _name;
        this.points = _points;
    }
}